    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
            int selectedRow = TableDichVu.getSelectedRow();

     if (selectedRow == -1) {
         javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng chọn dịch vụ để xóa.");
         return;
     }

     String maDV = TableDichVu.getValueAt(selectedRow, 0).toString(); // Cột 0 là Mã dịch vụ

     int confirm = javax.swing.JOptionPane.showConfirmDialog(this,
             "Bạn có chắc muốn xóa dịch vụ có mã: " + maDV + "?",
             "Xác nhận xóa",
             javax.swing.JOptionPane.YES_NO_OPTION);

     if (confirm == javax.swing.JOptionPane.YES_OPTION) {
         int success = DichVuDAO.deleteDichVu(maDV); // Gọi DAO xóa theo mã dịch vụ

         if (success > 0) {
             javax.swing.JOptionPane.showMessageDialog(this, "Xóa dịch vụ thành công.");
             refreshTable(); // Cập nhật lại bảng sau khi xóa
         } else {
             javax.swing.JOptionPane.showMessageDialog(this, "Xóa thất bại. Vui lòng thử lại.");
         }
     }
    }//GEN-LAST:event_jButton2ActionPerformed


