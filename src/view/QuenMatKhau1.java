    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String userOTP = ForgotPassword1.getText().trim();

    if (userOTP.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập mã OTP");
        return;
    }

    if (generatedOTP != null && userOTP.equals(generatedOTP)) {
        JOptionPane.showMessageDialog(this, "Xác minh thành công! Bạn có thể đổi mật khẩu.");
        // TODO: mở form đổi mật khẩu hoặc cho phép tiếp tục
     ManHinhDoiMatKhau1 doiMatKhauForm = new ManHinhDoiMatKhau1(ForgotPassword.getText().trim());
    
    doiMatKhauForm.setLocationRelativeTo(this);
    doiMatKhauForm.setVisible(true);
    this.dispose();
    } else {
        JOptionPane.showMessageDialog(this, "Mã OTP không đúng. Vui lòng thử lại.");
    }
    }//GEN-LAST:event_jButton2ActionPerformed

