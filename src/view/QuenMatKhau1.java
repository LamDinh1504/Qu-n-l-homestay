    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = ForgotPassword.getText().trim();

    if (email.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Vui lòng nhập email");
        return;
    }

    // Sinh mã OTP giả sử
     generatedOTP = String.valueOf((int)(Math.random() * 900000) + 100000);

    String subject = "Mã xác nhận đổi mật khẩu";
    String content = "Mã OTP của bạn là: " + generatedOTP;

    boolean result = EmailSender1.sendEmail(email, subject, content);
    if (result) {
        JOptionPane.showMessageDialog(this, "Đã gửi mã OTP đến email của bạn!");
        // Lưu OTP vào biến toàn cục hoặc mở form nhập OTP nếu muốn
    } else {
        JOptionPane.showMessageDialog(this, "Gửi email thất bại. Vui lòng kiểm tra lại.");
    }
    }//GEN-LAST:event_jButton1ActionPerformed

